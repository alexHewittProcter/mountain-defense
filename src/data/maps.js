export function getMapById(id) {
  return MAPS.find((m) => m.id === id);
}

export function nextMapId(id) {
  const i = MAPS.findIndex((m) => m.id === id);
  return i >= 0 && i < MAPS.length - 1 ? MAPS[i + 1].id : null;
}
